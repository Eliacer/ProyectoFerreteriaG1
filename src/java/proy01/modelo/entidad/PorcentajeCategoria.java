package proy01.modelo.entidad;

public class PorcentajeCategoria {


     private String idCategoria;
     private String idPorcGanancia;
     private Double valor;

    public PorcentajeCategoria() {
    }

    public String getIdCategoria() {
        return idCategoria;
    }

    public void setIdCategoria(String idCategoria) {
        this.idCategoria = idCategoria;
    }

    public String getIdPorcGanancia() {
        return idPorcGanancia;
    }

    public void setIdPorcGanancia(String idPorcGanancia) {
        this.idPorcGanancia = idPorcGanancia;
    }

    public Double getValor() {
        return valor;
    }

    public void setValor(Double valor) {
        this.valor = valor;
    }
    
   
    
}


